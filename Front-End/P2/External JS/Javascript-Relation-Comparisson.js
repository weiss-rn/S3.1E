var me = 20;
var you = 30;
// EQUAL
var result = me == you;
console.log(me == you);
document.write('${me} == ${you} = ${result}<br/>');

// NOT EQUAL
console.log(me != you);
document.write('${me} != ${you} = ${result}<br/>');

// GREATER THAN
console.log(me > you);
document.write('${me} > ${you} = ${result}<br/>');

// LESS THAN
console.log(me < you);
document.write('${me} < ${you} = ${result}<br/>');

// GREATER THAN OR EQUAL TO
console.log(me >= you);
document.write('${me} >= ${you} = ${result}<br/>');

// LESS THAN OR EQUAL TO
console.log(me <= you);
document.write('${me} <= ${you} = ${result}<br/>');
