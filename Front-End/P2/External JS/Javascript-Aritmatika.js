// Variable
var a = 20;
var b = 5;
var c = 0;

// Operator aritmatika
// Penambahan
c = a + b;
console.log('${a} + ${b} = ${c}');
document.write('${a} + ${b} = ${c}<br/>');

// Pengurangan
c = a - b;
console.log('${a} - ${b} = ${c}');
document.write('${a} - ${b} = ${c}<br/>');
// Perkalian
c = a * b;
console.log('${a} * ${b} = ${c}');
document.write('${a} * ${b} = ${c}<br/>');

// Pembagian
c = a / b;
console.log('${a} / ${b} = ${c}');
document.write('${a} / ${b} = ${c}<br/>');

// Modulus
c = a % b
console.log('${a} % ${b} = ${c}');
document.write('${a} % ${b} = ${c}<br/>');

// Pemangkatan
c = a ** b;
console.log('${a} ** ${b} = ${c}');
document.write('${a} ** ${b} = ${c}<br/>');

// Increment
c = a++;
console.log('${a}++ = ${c}');
document.write('${a}++ = ${c}<br/>');

// Decrement
c = a--;
console.log('${a}-- = ${c}');
document.write('${a}-- = ${c}<br/>');

